-module(db_app).

-behaviour(application).

-include("db.hrl").

%% Application callbacks
-export([start/2, stop/1]).

%%====================================================================
%% API
%%====================================================================

start(_StartType, _StartArgs) ->
    db_lib:install([node() | nodes()]),
    mnesia:start(),
    ok = mnesia:wait_for_tables(?TABLES, 5000),
    db_sup:start_link().

%%--------------------------------------------------------------------
stop(_State) ->
    ok.

