-module(db_app).

-behaviour(application).

%% Application callbacks
-export([start/2, stop/1]).

%%====================================================================
%% API
%%====================================================================

start(_StartType, _StartArgs) ->
    ok = application:ensure_started(lager),
    db_sup:start_link().

%%--------------------------------------------------------------------
stop(_State) ->
    ok = application:stop(lager).

